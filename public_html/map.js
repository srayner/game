Map.prototype.draw = function(context) {
    
    // Blue background
    context.fillStyle = 'rgba(87, 139, 210, 1)';
    context.fillRect(0, 0, 768, 512);
    
    for (var y=0; y<16; y++) {
        for (var x=0; x<24; x++) {
            if (this.data[y][x] === 1) {
                context.drawImage(this.img, 32 * x, 32 * y);
            }
        }
    }
   
};